    private void btnExcluirTemaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirTemaActionPerformed
        int row = tblTemas.getSelectedRow();
        
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um tema");
            return;
        }
        
        String tema = temas.get(row);
        
        int opcao = JOptionPane.showConfirmDialog(
            null,
            "Deseja mesmo excluir o tema \"" + tema + "\"? Todas as palavras associadas a ele ser√£o excluidas.",
            "Excluir tema",
            JOptionPane.YES_NO_OPTION
        );
        
        if (opcao == JOptionPane.YES_OPTION) {
            TemasProvider.excluirTema(tema);
            atualizar();
        }
    }//GEN-LAST:event_btnExcluirTemaActionPerformed

